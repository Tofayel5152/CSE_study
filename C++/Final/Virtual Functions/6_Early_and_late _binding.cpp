/*
    * Binding :
        When we compile our program, all variables and functions are mapped to addresses in memory. These addresses are then used for
        reference in the program. This process of converting variables and functions into addresses is known as binding. There are two
        types of binding such as Early Binding and Late Binding.

    * Early Binding VS Late Binding:
        1. Early Binding: 
            Early binding is also called static binding. Early binding occurs when we make the explicit or direct function call in our
            program. Early binding refers to events that occur at compile time. Early binding occurs when all information needed to call
            a function is known at compile time. Examples : function calls ,overloaded function calls, and overloaded operators. 
        2. Late Binding:
            Late binding is also called dynamic binding. Late binding occurs when we make implicit or indirect function calls in our program.
            Late binding refers to function calls that are not resolved until run time. Late binding can make for somewhat slower execution times.
            Example: virtual functions.
*/